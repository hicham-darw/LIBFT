#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n >= 0)
	{
		c = (n % 10) + 48;
		if (n >= 10)
			ft_putnbr_fd(n / 10, fd);
		write(fd, &c, 1);
	}
	else
	{
		write(fd, "-", 1);
		ft_putnbr_fd(-n, fd);
	}
}
